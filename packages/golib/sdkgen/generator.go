// GenerateGoSDK generates a Go SDK client from endpoint definitions.
func GenerateGoSDK(cfg SDKConfig) (string, error) {
	// Ensure path params are populated.
	for i := range cfg.Endpoints {
		if len(cfg.Endpoints[i].PathParams) == 0 {
			cfg.Endpoints[i].PathParams = ExtractPathParams(cfg.Endpoints[i].Path)
		}
	}

	funcMap := template.FuncMap{
		"methodName":            methodName,
		"methodParams":          methodParams,
		"methodReturn":          methodReturn,
		"hasBody":               hasBody,
		"zeroReturn":            zeroReturn,
		"pathFmt":               pathFmt,
		"pathArgs":              pathArgs,
		"pathParamReplacements": pathParamReplacements,
	}

	tmpl, err := template.New("sdk").Funcs(funcMap).Parse(sdkTemplate)
	if err != nil {
		return "", fmt.Errorf("parse template: %w", err)
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, cfg); err != nil {
		return "", fmt.Errorf("execute template: %w", err)
	}
	return buf.String(), nil
}

// GenerateGoTypes generates Go type definitions from endpoint descriptions.
func GenerateGoTypes(cfg SDKConfig) (string, error) {
	tmpl, err := template.New("types").Parse(typesTemplate)
	if err != nil {
		return "", fmt.Errorf("parse template: %w", err)
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, cfg); err != nil {
		return "", fmt.Errorf("execute template: %w", err)
	}
	return buf.String(), nil
}

func methodName(ep Endpoint) string {
	// Build a method name from the HTTP method and path.
	parts := strings.Split(strings.Trim(ep.Path, "/"), "/")
	var name string
	switch ep.Method {
	case "GET":
		name = "Get"
	case "POST":
		name = "Create"
	case "PUT":
		name = "Update"
	case "DELETE":
		name = "Delete"
	case "PATCH":
		name = "Patch"
	default:
		name = ep.Method
	}
	for _, p := range parts {
		if strings.HasPrefix(p, "{") {
			continue
		}
		// Skip common prefixes.
		if p == "api" || p == "v1" || p == "v2" {
			continue
		}
		name += capitalize(p)
	}
	return name
}

func capitalize(s string) string {
	if s == "" {
		return s
	}
	return strings.ToUpper(s[:1]) + s[1:]
}

func methodParams(ep Endpoint) string {
	var params []string
	for _, p := range ep.PathParams {
		params = append(params, toCamelCase(p)+" string")
	}
	if hasBody(ep) {
		params = append(params, "reqBody "+ep.RequestType)
	}
	return strings.Join(params, ", ")
}

func toCamelCase(s string) string {
	parts := strings.Split(s, "_")
	for i := 1; i < len(parts); i++ {
		parts[i] = capitalize(parts[i])
	}
	return strings.Join(parts, "")
}

func methodReturn(ep Endpoint) string {
	if ep.ResponseType != "" {
		return "(" + ep.ResponseType + ", error)"
	}
	return "error"
}

func hasBody(ep Endpoint) bool {
	return ep.RequestType != "" && (ep.Method == "POST" || ep.Method == "PUT" || ep.Method == "PATCH")
}

func zeroReturn(ep Endpoint) string {
	if ep.ResponseType != "" {
		return ep.ResponseType + "{}, "
	}
	return ""
}

func pathFmt(ep Endpoint) string {
	result := ep.Path
	for _, p := range ep.PathParams {
		result = strings.Replace(result, "{"+p+"}", "%s", 1)
	}
	return result
}

func pathArgs(ep Endpoint) string {
	var args []string
	for _, p := range ep.PathParams {
		args = append(args, toCamelCase(p))
	}
	return strings.Join(args, ", ")
}

func pathParamReplacements(ep Endpoint) bool {
	return len(ep.PathParams) > 0
}
