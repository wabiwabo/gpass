// Package sdkgen generates Go SDK client code from API route definitions.
package sdkgen

import (
	"bytes"
	"fmt"
	"strings"
	"text/template"
)

// Endpoint describes an API endpoint for code generation.
type Endpoint struct {
	Method       string   // GET, POST, etc.
	Path         string   // /api/v1/sign/certificates
	Description  string
	RequestType  string // Go type name for request body
	ResponseType string // Go type name for response
	AuthRequired bool
	PathParams   []string // extracted from {param} in path
}

// SDKConfig for generating SDK code.
type SDKConfig struct {
	PackageName string
	ServiceName string
	BaseURL     string
	Endpoints   []Endpoint
}

// ExtractPathParams extracts path parameters from a URL path pattern.
// E.g., "/api/v1/users/{user_id}/certs/{cert_id}" returns ["user_id", "cert_id"].
func ExtractPathParams(path string) []string {
	var params []string
	for {
		start := strings.Index(path, "{")
		if start == -1 {
			break
		}
		end := strings.Index(path[start:], "}")
		if end == -1 {
			break
		}
		params = append(params, path[start+1:start+end])
		path = path[start+end+1:]
	}
	return params
}

